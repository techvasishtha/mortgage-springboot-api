
CREATE TABLE User(USER_ID INT PRIMARY KEY, FIRST_NAME VARCHAR(50), LAST_NAME VARCHAR(50), DATE_OF_BIRTH DATE, EMAIL_ADDRESS VARCHAR(50), PASSWORD VARCHAR(50), ADDRESS VARCHAR(50), CELL_PHONE_NUMBER VARCHAR(15));



CREATE TABLE Mortgage_Info(ID INT PRIMARY KEY, USER_ID INT, EMPLOYER_NAME VARCHAR(50), JOB_TITLE VARCHAR(50), YEARS_OF_SERVICE VARCHAR(10),SALARAY INT, ADDRESS_LINE1 VARCHAR(250), ADDRESS_LINE2 VARCHAR(250), CITY VARCHAR(50), PROVINCE VARCHAR(50), COUNTRY VARCHAR(50), PURCHASE_PRICE INT, DOWN_PAYMENT INT, MORTGAGE_AMOUNT INT, CLOSING_DATE DATE);